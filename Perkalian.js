// Fungsi untuk menghitung perkalian
function hitungPerkalian(angka1, angka2) {
    return angka1 * angka2;
}

// Memanggil fungsi perkalian
let hasil1 = hitungPerkalian(5, 3);
let hasil2 = hitungPerkalian(10, 7);
let hasil3 = hitungPerkalian(8, 4);

// Menampilkan hasil
console.log("5 × 3 = " + hasil1);      // Output: 5 × 3 = 15
console.log("10 × 7 = " + hasil2);     // Output: 10 × 7 = 70
console.log("8 × 4 = " + hasil3);      // Output: 8 × 4 = 32