// 1. LOOP FOR
console.log("=== LOOP FOR ===");
for (let i = 1; i <= 10; i++) {
    console.log(i);
}

// 2. LOOP WHILE
console.log("\n=== LOOP WHILE ===");
let j = 1;
while (j <= 10) {
    console.log(j);
    j++;
}

// 3. LOOP DO...WHILE
console.log("\n=== LOOP DO...WHILE ===");
let k = 1;
do {
    console.log(k);
    k++;
} while (k <= 10);